function fechaEnMesCalendarioActual(fechaIso: string, referencia: Date): boolean {
  const d = new Date(`${fechaIso}T12:00:00`)
  return d.getFullYear() === referencia.getFullYear() && d.getMonth() === referencia.getMonth()
}

export function calcularIndicadoresSocios(socios: Socio[], referencia: Date = new Date()): IndicadoresSocios {
  return {
    totalSocios: socios.length,
    sociosActivos: socios.filter((s) => s.estado === 'activo').length,
    sociosAlDia: socios.filter((s) => s.estadoCuota === 'al-dia').length,
    sociosMorosos: socios.filter((s) => s.estadoCuota === 'moroso' || s.estadoCuota === 'vencido').length,
    pagosAlDiaMesActual: socios.filter((s) => fechaEnMesCalendarioActual(s.fechaUltimoPago, referencia)).length,
  }
}
